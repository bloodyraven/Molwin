package bean;

public class Statut {

	private int vie;
	private int vieMax;
	private int mana;
	private int esq;
	private int cc; 
	private int force;
	private int intel;
	private int def;
	private int magicDef;
	
	public int getVie() {
		return vie;
	}
	public void setVie(int vie) {
		this.vie = vie;
	}
	public int getVieMax() {
		return vieMax;
	}
	public void setVieMax(int vieMax) {
		this.vieMax = vieMax;
	}
	public int getMana() {
		return mana;
	}
	public void setMana(int mana) {
		this.mana = mana;
	}
	public int getEsq() {
		return esq;
	}
	public void setEsq(int esq) {
		this.esq = esq;
	}
	public int getCc() {
		return cc;
	}
	public void setCc(int cc) {
		this.cc = cc;
	}
	public int getForce() {
		return force;
	}
	public void setForce(int force) {
		this.force = force;
	}
	public int getIntel() {
		return intel;
	}
	public void setIntel(int intel) {
		this.intel = intel;
	}
	public int getDef() {
		return def;
	}
	public void setDef(int def) {
		this.def = def;
	}
	public int getMagicDef() {
		return magicDef;
	}
	public void setMagicDef(int magicDef) {
		this.magicDef = magicDef;
	}
	
	
}
